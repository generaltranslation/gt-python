def _js_options_to_kwargs(options: dict) -> dict:
    """Convert JS-style options (camelCase) to Python kwargs (snake_case)."""
    kwargs = {}
    if "context" in options:
        kwargs["context"] = options["context"]
    if "id" in options:
        kwargs["id"] = options["id"]
    if "maxChars" in options:
        kwargs["max_chars"] = options["maxChars"]
    return kwargs


